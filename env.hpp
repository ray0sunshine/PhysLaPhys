pt3 calcNorm(pt3 a, pt3 b){
	pt3 ret;
	ret.x = a.y*b.z-a.z*b.y;
	ret.y = a.z*b.x-a.x*b.z;
	ret.z = a.x*b.y-a.y*b.x;
	double len = sqrt(ret.x*ret.x + ret.y*ret.y + ret.z*ret.z);
	ret.x /= len;
	ret.y /= len;
	ret.z /= len;
	return ret;
}

